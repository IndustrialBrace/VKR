# ---------------------------------------------------------------------------
# Построение mapping старый -> новый и текста списка
# ---------------------------------------------------------------------------

def build_mapping_and_listing():
    mapping: dict[int, int] = {}
    lines: list[str] = []
    new_num = 0
    for entry in BIBLIOGRAPHY:
        old, body = entry
        if old == "__SECTION__":
            lines.append("")
            lines.append(f"### {body}")
            lines.append("")
            continue
        new_num += 1
        if old in mapping:
            raise ValueError(f"Дубликат старого номера: {old}")
        mapping[int(old)] = new_num
        lines.append(f"{new_num}. {body}")
        lines.append("")
    return mapping, "\n".join(lines)


# ---------------------------------------------------------------------------
# Перенумерация ссылок [N, ...] в тексте
# ---------------------------------------------------------------------------

# Шаблон ссылочного блока вида [N], [N, с. X], [N, с. X; M, с. Y; ...]
# Внутри запрещаем переносы строк.
REF_RE = re.compile(r"\[((?:\d+[^\[\]\n]*?))\]")

# Внутри блока — отдельные пункты, разделённые ;
# В каждом пункте номер источника — первое число.
ITEM_NUM_RE = re.compile(r"^(\s*)(\d+)(.*)$", re.DOTALL)


def renumber_refs(text: str, mapping: dict[int, int]) -> tuple[str, list[int]]:
    """Заменяет в тексте ссылки на источники по mapping.
    Возвращает новый текст и список номеров, которые встретились,
    но отсутствуют в mapping (для диагностики)."""
    missing: set[int] = set()

    def repl(m: re.Match) -> str:
        inner = m.group(1)
        # Пропускаем markdown-ссылки на файлы — там после ] идёт (
        # Но мы внутри [...] не знаем что дальше; поэтому проверим в внешнем
        # коде (см. ниже), здесь работаем только с уже отфильтрованным.
        parts = inner.split(";")
        new_parts: list[str] = []
        any_changed = False
        for part in parts:
            mm = ITEM_NUM_RE.match(part)
            if not mm:
                new_parts.append(part)
                continue
            lead, num_s, tail = mm.group(1), mm.group(2), mm.group(3)
            n = int(num_s)
            if n in mapping:
                new_parts.append(f"{lead}{mapping[n]}{tail}")
                if mapping[n] != n:
                    any_changed = True
            else:
                missing.add(n)
                new_parts.append(part)
        if not any_changed and not missing:
            return m.group(0)
        return "[" + ";".join(new_parts) + "]"

    # Чтобы не задеть markdown-ссылки [text](url), обрабатываем только
    # такие [...], после которых НЕ идёт «(».
    out = []
    i = 0
    while i < len(text):
        m = REF_RE.search(text, i)
        if not m:
            out.append(text[i:])
            break
        out.append(text[i:m.start()])
        # Проверка: не markdown-ссылка ли это
        end = m.end()
        if end < len(text) and text[end] == "(":
            # Это markdown-ссылка: оставляем как есть
            out.append(m.group(0))
        else:
            # Это библиографическая ссылка — обрабатываем
            out.append(repl(m))
        i = end
    return "".join(out), sorted(missing)


# ---------------------------------------------------------------------------
# Главная функция
# ---------------------------------------------------------------------------

def main() -> None:
    mapping, listing = build_mapping_and_listing()
    print("=== Mapping старый -> новый ===")
    for old in sorted(mapping):
        print(f"  {old:>2} -> {mapping[old]:>2}")
    print(f"Итого пунктов: {len(mapping)}")

    text = SRC_FILE.read_text(encoding="utf-8")

    new_text, missing = renumber_refs(text, mapping)
    if missing:
        print("ВНИМАНИЕ: ссылки на отсутствующие в mapping номера:")
        print("  ", missing)

    # Заменяем последний раздел (после "Заключение") — добавляем список.
    # Удалим возможный старый «список использованных источников», если есть.
    marker = "# Список использованных источников"
    if marker in new_text:
        new_text = new_text.split(marker, 1)[0].rstrip() + "\n\n"

    # Убедимся, что файл заканчивается одним разделителем перед списком.
    # Сначала срежем все хвостовые --- и пустые строки.
    new_text = re.sub(r"(?:\s*---\s*)+\Z", "\n", new_text.rstrip()) + "\n\n---\n\n"
    new_text += "# Список использованных источников\n\n"
    new_text += (
        "_Список оформлен в соответствии с ГОСТ Р 7.0.100-2018 и "
        "Положением о выпускной квалификационной работе ИУЭиФ КФУ. "
        "Внутри разделов соблюдён алфавитный порядок (для нормативных "
        "правовых актов — по юридической силе и дате принятия); внутри "
        "ВКР используется сквозная нумерация арабскими цифрами._\n"
    )
    new_text += listing
    new_text = new_text.rstrip() + "\n"

    SRC_FILE.write_text(new_text, encoding="utf-8")
    print(f"OK: записан файл {SRC_FILE} ({len(new_text)} символов)")


if __name__ == "__main__":
    main()
